class PromptBuilder:
    def build(self, context: str, question: str) -> list:
        return [
            {
                "role": "system",
                "content": (
                    """Ты — научный ассистент.
                    Отвечай строго на основе предоставленного контекста.

                    ТРЕБОВАНИЯ:
                    - обязательно указывай источники в формате [номер]
                    - не выдумывай источники
                    - используй только приведённый контекст
                    - стиль: академический, технический
                    - язык ответа: русский
                    - если нет ответа в контексте, говори, что нет информации по этому вопросу
                    """
                ),
            },
            {
                "role": "user",
                "content": f"""
КОНТЕКСТ:
{context}

ВОПРОС:
{question}
""",
            },
        ]
